    public class CalculosDeArea
    {
        /// <summary>
        /// Metaodo para calcular a area de um quadrado
        /// </summary>
        /// <param name="lado">Numero do lado do quadrado</param>
        /// <returns>area do quadrado</returns>
        public double CalculaAreaQuadrado (double lado)
        {
            return lado * lado;
        }
    }
